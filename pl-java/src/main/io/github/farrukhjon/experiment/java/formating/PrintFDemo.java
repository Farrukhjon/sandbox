package io.github.farrukhjon.experiment.java.formating;

public class PrintFDemo {

  public static void main(String[] args) {
    System.out.printf("%-17s %1s %n", "Hello", 1234567890);
    System.out.printf("%-17s %1s %n", "Hellooooo", 123456790);
    System.out.printf("%-17s %1s %n", "Heeellooooo", 123456790);
    System.out.printf("%-17s %1s %n", "Hellllllooooo", 123456790);
    System.out.printf("%-17s %1s %n", "Hhhheeeellllooooo", 123456790);
  }

}
