package io.github.farrukhjon.experiment;

public class Book {

    private Long id;

    private String name;

    public Book(final Long id, final String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
